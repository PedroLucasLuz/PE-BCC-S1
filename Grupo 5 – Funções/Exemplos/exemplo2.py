
### Primeiro deve-se definir a variável como função, usando a palavra-chave "def".
### Depois de definir a função, colocar os parâmetros necessários entre os parênteses.
def saudar_usuario(nome):
    ### Após isso coloca o código que será executado sempre que a função for utilizada.
    print(f"Olá, {nome}! Bem-vindo ao curso de Python.")
    ### Dessa vez não é necessário utilizar o return, pois a função imprime a saudação diretamente em seu código.

### Agora, chama a função passando o parâmetro nome como "Juvenal".
saudar_usuario("Juvenal")
### Depois de utilizar a função, "Olá, Juvenal! Bem-vindo ao curso de Python." aparecerá no console.