
### Exemplo de função que recebe um parâmetro e retorna uma string formatada
# Primeiro deve ser definida a função, utilizando a palavra-chave "def"
# Após a definição, você deve colocar o bloco de código que será executado sempre a função for chamada.
# O bloco de código deve ser identado (geralmente com 4 espaços ou um tab)
def funcao_saudacao(nome):
    return f"Saudações, {nome}."

# Agora que a função foi criada, podemos chamá-la
# O parâmetro nome é passado entre os parênteses da função
# O resultado da função é retornado e pode ser salvo em alguma variável
# Porém neste caso, vamos apenas imprimir o resultado diretamente no console.
print(funcao_saudacao("Jerivaldo"))

# O resultado da execução do código acima será:
# Saudações, Jerivaldo.