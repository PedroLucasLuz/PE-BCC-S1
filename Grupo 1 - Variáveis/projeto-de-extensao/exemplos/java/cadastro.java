public class cadastro {
    public static void main(String[] args) {

        // String -> armazena o nome do funcionário
        String nome = "Pedro Henrique";

        // int -> armazena a idade do funcionário
        int idade = 29;

        // double -> armazena o salário do funcionário
        double salario = 3800.00;

        // char -> armazena o nível do funcionário
        char nivel = 'c';

        // boolean -> informa se o funcionário está ativo
        boolean ativo = true;

        // Exibição dos dados
        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade);
        System.out.println("Salário: R$ " + salario);
        System.out.println("Nível: " + nivel);
        System.out.println("Ativo: " + ativo);
    }
}