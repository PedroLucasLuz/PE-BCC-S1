# String -> armazena o nome do funcionário
nome = "Pedro Henrique"

# int -> armazena a idade do funcionário
idade = 29

# double (float) -> armazena o salário do funcionário
salario = 3800.00

# char -> armazena o nível do funcionário (Python trata como String de 1 caractere)
nivel = 'B'

# boolean -> informa se o funcionário está ativo
ativo = True

# Exibição dos dados
print("Nome: " + nome)
print("Idade: " + str(idade))
print("Salário: R$ " + str(salario))
print("Nível: " + nivel)
print("Ativo: " + str(ativo))